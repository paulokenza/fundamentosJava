package fundamentos;

/**
 * Classe responsavel por mostrar os 3 tipos de comentarios que o java suporta
 * @author Paulo Henrique [paulokenza@gmail.com]
 * @version 1.0 
 * @since 2.2
 */
public class Comentarios {
	
	/**
	 * Esse método é a porta de entrada de um programa Java.
	 * @param args parametros passados por linha de comando
	 */
	public static void main(String[] args) {
		
		/*
		 * Comentario de varias linhas
		 * Imprime no console a palavra primeiro e
		 * em seguida cria uma nova linha
		 */
		System.out.println("Primeiro");
		
		//Cuidado com os comentários óbvios
		System.out.println("Programa!");
	}

}
